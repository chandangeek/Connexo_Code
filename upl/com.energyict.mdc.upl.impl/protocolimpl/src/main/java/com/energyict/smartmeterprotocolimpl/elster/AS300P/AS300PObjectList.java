    /**
     * Usage example:
     * UniversalObject[] buffer = ((AS300P) getProtocol()).getObjectFactory().getAssociationLnObject(0).getBuffer();
     * AS300PObjectList.printObjectList(buffer);
     */
    public static void printObjectList(UniversalObject[] objects) {
        List<String> entries = new ArrayList<String>();

        for (UniversalObject object : objects) {

            String uoString = "new UniversalObject(ObisCode.fromString(\"" + object.getObisCode() + "\"), DLMSClassId." + object.getDLMSClassId().name() + "),";
            if (!entries.contains(uoString) && !object.getDLMSClassId().equals(DLMSClassId.UNKNOWN)) {
                entries.add(uoString);
            }
        }

        System.out.println("\r\n\r\n\r\n");
        for (int i = 0; i < entries.size(); i++) {
            System.out.println(entries.get(i));
        }
    }

